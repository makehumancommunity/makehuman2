#!/usr/bin/python3
"""
export a user base as a zipfile
"""

import os
import re
import shutil
import sys
import argparse
import tempfile

class zipBase():
    def __init__(self, tmp, base, userdir, verbose, models):
        self.subdirs = ["base", "contarget", "clothes", "eyebrows", "eyes", "hair", "poses", "rigs", "skins", "teeth", "tongue", "expressions", "eyelashes",  "proxy" ]
        self.tmp = tmp
        self.base = base
        self.userdir = userdir
        self.verbose= verbose
        if models:
            self.subdirs.append("models")

        self.zipname = "newbase_" + base
        self.destination = os.path.join(self.tmp, "zipfolder_" + self.base)
        self.content = os.path.join(self.destination, "content")
        self.ignorefiles = ["^\\..*", ".*\\.mhbin", ".*\\.npz"]

    def cleanexit(self, num, text):
        print (text)
        exit(num)

    def mkdir(self,folder):
        if self.verbose:
            print ("+ check and create folder " + folder)
        if not os.path.isdir(folder):
            if os.path.isfile(folder):
                self.cleanexit(2, "File exists instead of folder " + folder)
            try:
                os.mkdir(folder)
            except OSError as error:
                self.cleanexit(2, str(error))

    def copyfile(self, source, dest):
        if self.verbose:
            print ("+ copy " + source + " to " + dest)
        try:
            shutil.copyfile(source, dest)
        except OSError as error:
            self.cleanexit(2, str(error))
        
    def copyRepo(self):

        # test if user-dir exists, then test if base is inside user-dir
        #
        if not os.path.isdir(self.userdir):
            self.cleanexit(2, self.userdir + " is not a directory!")

        basefolder = os.path.join(self.userdir, "base", self.base)
        if not os.path.isdir(basefolder):
            self.cleanexit(2, basefolder + " is not a directory!")

        # create the zipfoler
        #
        self.mkdir (self.destination)
        self.mkdir (self.content)

        for folder in self.subdirs:
            source = os.path.join(self.userdir, folder, self.base)
            if os.path.isdir(source):
                destination = os.path.join(self.content, folder)
                self.mkdir (destination)

                l = len(source)
                for root, dirs, files in os.walk(source, topdown=True):

                    if root.startswith(source):
                        destdir = os.path.join(destination, root[l+1:])

                    for elem in dirs:
                        self.mkdir(os.path.join(destdir, elem))

                    if os.path.isdir(destdir):
                        for elem in files:
                            dontcreate = False
                            for pat in self.ignorefiles:
                                if re.match(pat, elem):
                                    dontcreate = True

                            if not dontcreate:
                                self.copyfile(os.path.join(root, elem), os.path.join(destdir, elem))

    def createZipFile(self):
        shutil.make_archive(os.path.join(self.destination, self.zipname), "zip", self.content)


if __name__ == '__main__':
    parser = argparse.ArgumentParser(description="Build a newbase_<base>.zip from user assets")
    parser.add_argument("base", type=str, help="name of the base")
    parser.add_argument("userdir", type=str, help="absolute path of the user documents")
    parser.add_argument("builddir", type=str, nargs='?',
        help="where to build the package, default is an autogenerated temporary directory")
    parser.add_argument("--verbose", "-v", action='store_true',  help="verbose")
    parser.add_argument("--models", "-m", action='store_true',  help="also add models")

    args = parser.parse_args()
    if not args.builddir:
        args.builddir = tempfile.gettempdir()
    if args.verbose:
        print ("+ working with " + args.builddir)

    zB = zipBase(args.builddir, args.base, args.userdir, args.verbose, args.models)
    zB.copyRepo()
    zB.createZipFile()

exit(0)
